    /**
     * Each LGA entry: [name, lat, lng, postal_code]
     * Coordinates are centroids of the LGA / major town.
     * Postal codes follow NIPOST format.
     */
    public function run(): void
    {
        $data = $this->getLgaData();

        $stateCache = [];

        foreach ($data as $stateName => $lgas) {
            if (!isset($stateCache[$stateName])) {
                $stateCache[$stateName] = State::where('name', $stateName)->first();
            }

            $state = $stateCache[$stateName];

            if (!$state) {
                $this->command->warn("State not found: {$stateName}");
                continue;
            }

            $count = 0;
            foreach ($lgas as [$lgaName, $lat, $lng, $postalCode]) {
                Lga::updateOrCreate(
                    ['name' => $lgaName, 'state_name' => $stateName],
                    [
                        'slug' => Str::slug($lgaName),
                        'state_slug' => $state->slug,
                        'lat' => $lat,
                        'lng' => $lng,
                        'postal_code' => $postalCode,
                        'country_code' => 'NG',
                    ]
                );
                $count++;
            }

            // Update lga_count on state
            $state->update(['lga_count' => $count]);
            $this->command->info("{$stateName}: {$count} LGAs seeded.");
        }
    }

